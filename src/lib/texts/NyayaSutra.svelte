51.1: sādharmyavaidharmyotkarṣāpakarṣavarṇyāvarṇyavikalpasādhyaprāpty aprāptipraṣaṅgapratidṛṛṣṭāntānutpattisaṃśayaprakaraṇahetvarthā pattyaviśeṣopapattyupalabdhyanupalabdhinityānityakaāryasamāḥ

(siddhānta-sūtra)

51.2: sādharmyavaidharmyābhyāmupasaṃhāre taddharmaviparyayopapatteḥ sādharmyavaidharmyasamau

(siddhānta-sūtra)

51.3: gotvād gosiddhivat tatsiddhiḥ

(siddhānta-sūtra)</p>

<p>utkarṣasamādijātiṣaṭkaprakaraṇam(4-6)ṭa

51.4: sādhyadṛṣṭāntayordharmavikalpādubhayasādhyatvāccotkarṣāpakarṣa varṇyavikalpasādhyasamāḥ

(siddhānta-sūtra)

51.5: kiñcitsādharmyādupasaṃhārasiddhervaidharmyādapratiṣedhaḥ

(siddhānta-sūtra)

51.6: sādhyātideśācca dṛṣṭāntopaptteḥ

(siddhānta-sūtra)</p>

<p>[prāptyaprāptisamajātidvayaprakaraṇam(7-8)]

51.7: prāpya sādhyamaprāpya vā hetoḥ prāptyāviśiṣṭatvādaprāptyāsādhāka tvācca prāptyaprāptisamau

(siddhānta-sūtra)

51.8: ghaṭādiniṣpattidarśanāt pīḍane cābhicārādapratiṣedhaḥ

(siddhānta-sūtra)</p>

<p>[prasaṅgapratidṛṣṭāntasamaprakaraṇam(9-11)]

51.9: dṛṣṭāntasya kāraṇānapadeśāt pratyavasthānācca pratidṛṣṭāntena prasaṅgapratidṛṣṭāntasamau

(siddhānta-sūtra)

51.10: pradīpopādānaprasaṅganivṛttivat tadvinivṛttiḥ

(siddhānta-sūtra)

51.11: pratidṛṣṭāntahetutve ca nāheturdṛṣṭāntaḥ

(siddhānta-sūtra)</p>

<p>[anutpattisamaprakaraṇam(12-13)]

51.12: prāgutpatteḥ kāraṇābhāvādanutpattisamaḥ

(siddhānta-sūtra)

51.13: tathābhāvādutpannasya kāraṇopapatterna kāraṇapratiṣedhaḥ

(siddhānta-sūtra)</p>

<p>[saṃśayasamaprakaraṇam(14-15)]

51.14: sāmānyadṛṣṭāntayoraindriyakatve samāne nityānityasādharmyāt saṃśayasamaḥ

(siddhānta-sūtra)

51.15: sādharmyātsaṃśaye na saṃśayo vaidharmyādubhayathā vā saṃśaye 'tyanta saṃśayaprasaṅgo nityatvānabhyūpagamācca sāmānyasyāpratiṣedhaḥ

(siddhānta-sūtra)</p>

<p>[prakaraṇasamaprakaraṇam(16-17)]

51.16: ubhayasādharmyāt prakriyāsiddheḥ prakaraṇasamaḥ

(siddhānta-sūtra)

51.17: pratipakṣāt prakaraṇasiddheḥ pratiṣedhānupapattiḥ pratipakṣopapatteḥ

(siddhānta-sūtra)</p>

<p>[ahetusamaprakaraṇam(18-20)]

51.18: traikālyāsiddherhetorahetusamaḥ

(siddhānta-sūtra)

51.19: na hetutaḥ sādhyasiddhestraikālyāssiddhiḥ

(siddhānta-sūtra)

51.20: pratiṣedhānupapatteśca pratiṣeddhavyāpratiṣedhaḥ

(siddhānta-sūtra)</p>

<p>[arthāpattisamaprakaraṇam(21-22)]

51.21: arthāpattitaḥ pratipakṣasiddherarthāpattisamaḥ

(siddhānta-sūtra)

51.22: anuktasyārthāpatteḥ pakṣahānerupapattiranuktatvādanaikāntikatvāc cārthāpatteḥ

(siddhānta-sūtra)</p>

<p>[aviśeṣasamaprakaraṇam(23-24)]

51.23: ekadharmopapatteraviśeṣe sarvāviśeṣaprasaṅgātsadbhābopapatter-

aviśeṣasamaḥ

(siddhānta-sūtra)

51.24: kvaciddharmānupapatteḥ kvaciccopapatteḥ pratiṣedhābhāvaḥ

(siddhānta-sūtra)</p>

<p>[uapapattisamaprakaraṇam(25-26)]

51.25: ubhayokāraṇopapatterupapattisamaḥ

(siddhānta-sūtra)

51.26: uapapattikāraṇābhyaniñjānādapratiṣedhaḥ

(siddhānta-sūtra)</p>

<p>[upalabdhisamaprakaraṇam(27-28)]

51.27: nirdiṣṭakāraṇābhāve 'pyupalambhādupalabdhisamaḥ

(siddhānta-sūtra)

51.28: kāraṇāntarādapi taddharmopapatterapratiṣedhaḥ

(siddhānta-sūtra)</p>

<p>[anupalabdhisamaprakaraṇam(29-31)]

51.29: tadanupalabdheranupalambhādabhāvasiddhau tadviparītopapatter anupalabdhisamaḥ

(siddhānta-sūtra)

51.30: anupalambhātmakatvādanupalabdherahetuḥ

(siddhānta-sūtra)

51.31: jñānavikalpānāṃ ca bhāvābhāvasaṃvedanādadhyātmam

(siddhānta-sūtra)</p>

<p>[anityasamaprakaraṇam(32-34)

51.32: sādharmyāttulyadharmopapatteḥ sarvānityatvaprasaṅgādanityasamaḥ

(siddhānta-sūtra)

51.33: sādharmyādasiddheḥ pratiṣedhāsiddhiḥ pratiṣedhyasādharmyācca

(siddhānta-sūtra)

51.34: dṛṣṭānte ca sādhyasādhanabhāvena prajñātasya dharmasya tasysa cobhayathā bhāvānnāviśeṣaḥ

